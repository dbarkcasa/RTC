'''
This program uses the built-in max() and min() functions to find the highest and lowest values in listA.
'''

# list
listA = [23, 12, 45, 22, 11, 78, 13, 90, 45, 88, 10, -5, -1]

# Built-in functions used on list
max_num = max(listA)
min_num = min(listA)

# Print statements for results
print("Maximum number:", max_num)
print("Minimum number:", min_num)